package com.hust.searchengine.Entity;

public class FieldCount {
    public String field;
    public Integer count;

    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
